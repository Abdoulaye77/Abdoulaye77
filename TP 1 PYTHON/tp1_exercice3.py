j=int(input("Saisir le numéro du jour : "))
 
if(j == 1):
    print("Lundi")
elif(j == 2):
    print("Mardi")
elif(j == 3):
    print("Mercredi")
elif(j == 4):
    print("Jeudi")
elif(j == 5):
    print("Vendredi")
elif(j == 6):
    print("Samedi")
elif(j == 7):
    print("Dimanche")
else:
    print("Attention! Veuillez saisir le numéro de jour entre 1 et 7.")
